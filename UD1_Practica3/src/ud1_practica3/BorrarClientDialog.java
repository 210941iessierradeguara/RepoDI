    private void jButtonAceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptActionPerformed
        ListIterator<Cliente> it = clientesDialog.clientes.listIterator();
        while(it.hasNext())
        {
            Cliente client = it.next();
            if(client.getTelefono().equals(jTextFieldTelf.getText().toString()))
            {
                it.remove();
            }
        }
        
        clientesDialog.jComboBoxClientes.removeAllItems();
        clientesDialog.anyadirCombo();
        clientesDialog.principal.datos.setClientes(clientesDialog.clientes);
        this.dispose();
    }//GEN-LAST:event_jButtonAceptActionPerformed

