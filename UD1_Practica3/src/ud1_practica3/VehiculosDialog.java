    /**
     * Si no hay vehiculos en la tabla o ninguno está seleccionado lanza errores
     * que invocan un OptionPane para el usuario.
     * 
     * Selecciona la matrícula del vehículo en la row seleccionada y crea un 
     * ListIterator. Mientras queden elementos en el ListIterator crea un 
     * Vehículo temportal con los datos del vehiculo en el momento actual del 
     * bucle. Si su matrícula es igual a la seleccionada lo elimina de la lista
     * el dtm elimina su row de la tabla y se actualiza el ArrayList de vehículos
     * de principal.datos
     * @param evt 
     */
    private void jButtonDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDelActionPerformed
        try {
        if (dtm.getRowCount() == 0) {
                throw new IllegalAccessException();
            }
            if (jTableVehi.getSelectedRow() < 0) {
                throw new Exception();
            }
        String matricula = dtm.getValueAt(jTableVehi.getSelectedRow(), 0).toString();
        ListIterator<Vehiculo> it = vehiculos.listIterator();
        
        while(it.hasNext())
        {
            Vehiculo vehiculo = it.next();
            if(vehiculo.getMatricula().equals(matricula))
            {
                it.remove();
            }
        }
        dtm.removeRow(jTableVehi.getSelectedRow());
        principal.datos.setVehiculos(vehiculos);
        
        } catch (IllegalAccessException ex) {
                JOptionPane.showMessageDialog(null, 
                        "Tabla vacía", "ERROR", 
                        JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, 
                    "Vehículo sin seleccionar", "ERROR", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDelActionPerformed

