    /**
     * Vacia la tabla de cualquier vehículo y pone las líneas a 0,
     * por cada vehículo en la lista mete los datos del vehiculo en un array 
     * que luego se reparte al añadir una row con los datos de dicho vehículo
     * settea el dtm a la tabla de nuevo.
     */
    public void anyadirVehi()
    {
        jTableVehi.removeAll();
        dtm.setRowCount(0);
        String[] vehiDatos = new String[4];
        for (Vehiculo vehiculo : vehiculos) {
            vehiDatos[0] = vehiculo.getMatricula();
            vehiDatos[1] = vehiculo.getModelo();
            vehiDatos[2] = vehiculo.getMarca();
            vehiDatos[3] = vehiculo.getTelfPropietario();
            
            dtm.addRow(vehiDatos);
        }
        jTableVehi.setModel(dtm);
    }
    
    
