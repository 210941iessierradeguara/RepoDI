    /**
     * Elimina la línea de la tabla seleccionada, tanot de la tabla como del 
     * array de reparaciones, en caso de no estarlo lanza un showDialog para 
     * advertir del hecho.
     */
    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        try {
        if (dtm.getRowCount() == 0) {
                throw new IllegalAccessException();
            }
            if (jTableRep.getSelectedRow() < 0) {
                throw new Exception();
            }
        String id = dtm.getValueAt(jTableRep.getSelectedRow(), 0).toString();
        ListIterator<Reparacion> it = reparaciones.listIterator();
        
        while(it.hasNext())
        {
            Reparacion r = it.next();
            if(r.getCodRep() == Integer.valueOf(id))
            {
                it.remove();
            }
        }
        dtm.removeRow(jTableRep.getSelectedRow());
        principal.datos.setReparaciones(reparaciones);
        
        } catch (IllegalAccessException ex) {
                JOptionPane.showMessageDialog(null, 
                        "Tabla vacía", "ERROR", 
                        JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, 
                    "Reparación sin seleccionar", "ERROR", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

