    /**
     * Vacia la tabla de cualquier reparacion y pone las lineas a 0,
     * por cada reparación, en la lista mete los datos de la reparación en un 
     * array que luego reparte al añadir una row con los datos de dicha 
     * reparación que settea el dtm a la tabla de nuevo.
     */
    public void anyadirRep()
    {
        jTableRep.removeAll();
        dtm.setRowCount(0);
        String[] repDatos = new String[9];
        
        for (Reparacion r : reparaciones) {
            repDatos[0] = Integer.toString(r.getCodRep());
            repDatos[1] = Integer.toString(r.getCodMeca());
            repDatos[2] = r.getMatriculaVehi();
            repDatos[3] = r.getTelfProp();
            repDatos[4] = Boolean.toString(r.isTerminada());
            repDatos[5] = Integer.toString(r.getHoras());
            
            repDatos[6] = r.getPiezasStr().toString();
            repDatos[7] = Integer.toString(r.getCantPiezas());
            repDatos[8] = Double.toString(r.getPrecioReparacion());
            
            dtm.addRow(repDatos);
        }
        jTableRep.setModel(dtm);
    }
    
    
