    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        if (jTextFieldNombre.getText() == null || jTextFieldNombre.getText() == ""
            || jTextFieldApellidos.getText() == null || jTextFieldApellidos.getText() == ""
            || jSpinnerEdad.getValue() == null || (int) jSpinnerEdad.getValue() >= -1){
            nombre = jTextFieldNombre.getText();
            apellidos = jTextFieldApellidos.getText();
            edad = (int) jSpinnerEdad.getValue();
            alumno = new Alumno(nombre, apellidos, edad);
        } else {
            jLabelMessage.setText("Uno o más campos están sin completar");
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

