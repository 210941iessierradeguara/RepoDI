package dto;

/**
 *
 * @author DAM2Alu5
 */
public class Persona {
    int edad;
    String nombre;
    String mail;

    public Persona(int edad, String nombre, String mail) {
        this.edad = edad;
        this.nombre = nombre;
        this.mail = mail;
    }
    
    public Persona() {
        this.nombre = nombre;
    }
    
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }
}
